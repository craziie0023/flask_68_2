import math

# -------------------------------------------------------------
# ฟังก์ชันคำนวณพื้นที่รูปเรขาคณิตพื้นฐาน
# -------------------------------------------------------------


def rectangle_area(width, height):
    """
    คำนวณพื้นที่สี่เหลี่ยมผืนผ้า
    สูตร: พื้นที่ = กว้าง × ยาว
    """
    return width * height


def trapezoid_area(top_base, bottom_base, height):
    """
    คำนวณพื้นที่สี่เหลี่ยมคางหมู
    สูตร: พื้นที่ = (ด้านคู่ขนานบน + ด้านคู่ขนานล่าง) × สูง / 2
    """
    return (top_base + bottom_base) * height / 2


def equilateral_triangle_area(side_length):
    """
    คำนวณพื้นที่สามเหลี่ยมด้านเท่า
    สูตร: (√3 / 4) × ด้าน²
    """
    return (math.sqrt(3) / 4) * (side_length ** 2)


# -------------------------------------------------------------
    # ส่วนแสดงตัวอย่างการใช้งานฟังก์ชัน
# -------------------------------------------------------------

if __name__ == "__main__":

    # ตัวอย่าง 1: พื้นที่สี่เหลี่ยมผืนผ้า
    rect_area = rectangle_area(5, 10)
    print(f"พื้นที่สี่เหลี่ยมผืนผ้า (5 x 10) = {rect_area}")

    # ตัวอย่าง 2: พื้นที่สี่เหลี่ยมคางหมู
    trap_area = trapezoid_area(8, 4, 6)
    print(f"พื้นที่สี่เหลี่ยมคางหมู (8, 4, สูง 6) = {trap_area}")

    # ตัวอย่าง 3: พื้นที่สามเหลี่ยมด้านเท่า
    tri_area = equilateral_triangle_area(7)
    print(f"พื้นที่สามเหลี่ยมด้านเท่า (ด้านยาว 7) = {tri_area}")
